package com.scapper.common;

public class Tutor {
	
	private String name;
	
	private UserConnections connections;
	
	private String experienceMonths;
	
	private String background;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public UserConnections getConnections() {
		return connections;
	}

	public void setConnections(UserConnections connections) {
		this.connections = connections;
	}

	public String getExperienceMonths() {
		return experienceMonths;
	}

	public void setExperienceMonths(String experienceMonths) {
		this.experienceMonths = experienceMonths;
	}

	public String getBackground() {
		return background;
	}

	public void setBackground(String background) {
		this.background = background;
	}
	
}
