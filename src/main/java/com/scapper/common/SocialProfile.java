package com.scapper.common;

public class SocialProfile {
	
	private String twitterHandle;
	
	private String facebookPageUrl;
	
	private String linkedInPageUrl;
	
	private String gitUserUrl;
	
	private String stackOverflowProfileUrl;

	public String getTwitterHandle() {
		return twitterHandle;
	}

	public void setTwitterHandle(String twitterHandle) {
		this.twitterHandle = twitterHandle;
	}

	public String getFacebookPageUrl() {
		return facebookPageUrl;
	}

	public void setFacebookPageUrl(String facebookPageUrl) {
		this.facebookPageUrl = facebookPageUrl;
	}

	public String getLinkedInPageUrl() {
		return linkedInPageUrl;
	}

	public void setLinkedInPageUrl(String linkedInPageUrl) {
		this.linkedInPageUrl = linkedInPageUrl;
	}

	public String getGitUserUrl() {
		return gitUserUrl;
	}

	public void setGitUserUrl(String gitUserUrl) {
		this.gitUserUrl = gitUserUrl;
	}

	public String getStackOverflowProfileUrl() {
		return stackOverflowProfileUrl;
	}

	public void setStackOverflowProfileUrl(String stackOverflowProfileUrl) {
		this.stackOverflowProfileUrl = stackOverflowProfileUrl;
	}
	
}
